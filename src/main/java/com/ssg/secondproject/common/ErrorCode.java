package com.ssg.secondproject.common;

public enum ErrorCode {
    EXAMPLE_ERROR(404, "Example");

    private int code;
    private String message;

    ErrorCode(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }
}